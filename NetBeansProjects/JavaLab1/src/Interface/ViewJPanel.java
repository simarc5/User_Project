           public ImageIcon ResizeImage(String ImagePath)
    {
        
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImage = img.getScaledInstance(100, 150, Image.SCALE_DEFAULT);
       // Image newImg = img.getScaledInstance(label1.getPreferredSize().width, label1.getPreferredSize().height, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        return image;
    }

