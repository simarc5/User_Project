    private void displayProduct(User product){
        
          String path = product.getImage();
        label.setIcon(ResizeImage(path));
        
    
        String profileName = product.getName();
        nameTxt.setText(profileName);

        int tel = product.getTelNum();
        telTxt.setText(String.valueOf((tel)));
        
         int dob = product.getDob();
        dateTxt.setText(String.valueOf((dob)));
        
        
        String email = product.getEmail();
        emailTxt.setText(email);
        
         int ssn = product.getSsn();
        descTxt1.setText(String.valueOf((ssn)));
        
         String linkedin = product.getLinkedin();
        linTxt.setText(linkedin);
        
        String health = product.getHealth();
        healthTxt.setText(health);
        
             int vel = product.getVehicle();
        banTxt.setText(String.valueOf((vel)));
        
             int li = product.getLicence();
        liTxt.setText(String.valueOf((li)));
        
             int bank = product.getBank();
        vehicleTxt.setText(String.valueOf((bank)));
        
 
    }
    
